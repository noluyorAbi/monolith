export const GLYPH_W = 5;
export const GLYPH_H = 7;
/** Blank columns between two glyphs. */
export const GLYPH_TRACKING = 1;

export interface Pixel {
  col: number;
  row: number;
}

/** Width of a rendered string, measured in font pixels. */
export function measureText(text: string): number {
  const n = normalise(text).length;
  if (n === 0) return 0;
  return n * GLYPH_W + (n - 1) * GLYPH_TRACKING;
}

function normalise(text: string): string[] {
  return [...text.toUpperCase()].map((c) => (GLYPHS[c] ? c : "-"));
}

/**
 * Rasterise text to pixel coordinates. Origin is bottom-left, so `row` counts
 * upward and can be fed straight into world space without a flip.
 */
export function rasterise(text: string): Pixel[] {
  const pixels: Pixel[] = [];
  let cursor = 0;
  for (const char of normalise(text)) {
    const rows = GLYPHS[char];
    for (let r = 0; r < GLYPH_H; r++) {
      const line = rows[r];
      for (let c = 0; c < GLYPH_W; c++) {
        if (line[c] === "#") pixels.push({ col: cursor + c, row: GLYPH_H - 1 - r });
      }
    }
    cursor += GLYPH_W + GLYPH_TRACKING;
  }
  return pixels;
}
